"""
Python Dates (파이썬 날짜)
파이썬에서 날짜는 그 자체로 데이터 타입이 아니지만, `datetime`이라는 모듈을 가져와서(import) 날짜를 날짜 객체(date objects)로 다룰 수 있습니다.
"""

print("--- 현재 날짜 및 시간 ---")
# 예제: datetime 모듈을 임포트하고 현재 날짜 표시하기
import datetime

x = datetime.datetime.now()
print("현재 날짜와 시간:", x)


"""
Date Output (날짜 출력)
위 예제의 코드를 실행하면 다음과 같은 결과가 나옵니다.
예: 2026-03-07 21:15:27.506677
이처럼 날짜에는 연, 월, 일, 시, 분, 초, 마이크로초가 포함됩니다.

datetime 모듈에는 날짜 객체에 대한 정보를 반환하는 많은 메서드가 있습니다.
"""

print("\n--- 연도 및 요일 반환 ---")
# 예제: 연도와 요일 이름 반환하기
x = datetime.datetime.now()

print("연도(year):", x.year)
print("요일(strftime):", x.strftime("%A"))


"""
날짜 객체 생성하기 (Creating Date Objects)
날짜를 직접 생성하기 위해 datetime 모듈의 datetime() 클래스(생성자)를 사용할 수 있습니다.
datetime() 클래스는 날짜를 생성하기 위해 3개의 필수 매개변수(연, 월, 일)가 필요합니다.
"""

print("\n--- 날짜 객체 생성 ---")
# 예제: 날짜 객체 직접 생성하기
x = datetime.datetime(2020, 5, 17)

print("생성된 날짜:", x)
# datetime() 클래스는 시간과 시간대(시간, 분, 초, 마이크로초, 시간대)에 대한 매개변수도 받을 수 있지만, 
# 이들은 선택 사항이며 기본값은 0(시간대의 경우 None)으로 설정됩니다.


"""
strftime() 메서드 (The strftime() Method)
datetime 객체에는 날짜 객체를 읽기 쉬운 문자열로 포맷(형식화)하는 메서드가 있습니다.
이 메서드는 strftime()이라고 불리며, 반환될 문자열의 포맷을 지정하는 'format'이라는 하나의 매개변수를 받습니다.
"""

print("\n--- strftime() 메서드 사용 ---")
# 예제: 전체 월 이름 표시하기
x = datetime.datetime(2018, 6, 1)

print("월 이름 (전체):", x.strftime("%B"))


"""
[주요 포맷 코드 레퍼런스 (Format Codes Reference)]
%a : 요일, 짧은 버전 (예: Wed)
%A : 요일, 전체 버전 (예: Wednesday)
%w : 숫자로 된 요일, 0은 일요일 (예: 3)
%d : 월의 일, 01-31 (예: 31)
%b : 월 이름, 짧은 버전 (예: Dec)
%B : 월 이름, 전체 버전 (예: December)
%m : 숫자로 된 월, 01-12 (예: 12)
%y : 연도, 짧은 버전 (예: 18)
%Y : 연도, 전체 버전 (예: 2018)
%H : 시간, 00-23 (예: 17)
%I : 시간, 00-12 (예: 05)
%p : AM/PM (예: PM)
%M : 분, 00-59 (예: 41)
%S : 초, 00-59 (예: 08)
%f : 마이크로초 (예: 548513)
%Z : 시간대 (예: CST)
%j : 연중 일수, 001-366 (예: 365)
%c : 지역 날짜 및 시간 출력 (예: Mon Dec 31 17:41:00 2018)
%x : 지역 날짜 출력 (예: 12/31/18)
%X : 지역 시간 출력 (예: 17:41:00)
"""
